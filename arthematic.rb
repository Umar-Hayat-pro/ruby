## if both the number in the equation are integer the output wil be integer and if any one of the input is a float the out will be in float.
a = 7 
b = 9

puts a + b
#output 16
puts a - b 
#output -2
puts a * b 
#output 63
puts a / b 
#output 0
puts a % b
#output 7
puts a ** b
#output 40353607

a = 7.0
b = 9

puts a + b
#output 16.0
puts a - b 
#output -2.0
puts a * b 
#output 63.0
puts a / b 
#output 0.7777777777777778
puts a % b
#output 7.0
puts a ** b
#output 40353607.0