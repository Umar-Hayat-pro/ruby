# to_s -> to string text
# to_f -> to float number 
# to_i -> to integer number
# to_c -> to complex number
# to_r -> to rational number

# Convert the integer 1 to a string
puts 1.to_s  # Output: "1" (String representation)

# Convert the integer 2 to a floating-point number
puts 2.to_f  # Output: 2.0 (Floating-point representation)

# Convert the integer 3 to an integer (no change in this case)
puts 3.to_i  # Output: 3 (Integer representation)

# Convert the integer 4 to a complex number
puts 4.to_c  # Output: (4+0i) (Complex representation)

# Convert the integer 5 to a rational number
puts 5.to_r  # Output: (5/1) (Rational representation)
